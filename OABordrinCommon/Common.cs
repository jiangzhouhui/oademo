        /// <summary>
        /// 用于EF动态排序
        /// </summary>
        public static IQueryable<TEntity> OrderBy<TEntity>(this IQueryable<TEntity> source, string orderByProperty, bool desc) where TEntity : class
        {
            string command = desc ? "OrderByDescending" : "OrderBy";
            var type = typeof(TEntity);
            var property = type.GetProperty(orderByProperty);
            var parameter = Expression.Parameter(type, "p");
            var propertyAccess = Expression.MakeMemberAccess(parameter, property);
            var orderByExpression = Expression.Lambda(propertyAccess, parameter);
            var resultExpression = Expression.Call(typeof(Queryable), command, new Type[] { type, property.PropertyType }, source.Expression, Expression.Quote(orderByExpression));
            return source.Provider.CreateQuery<TEntity>(resultExpression);
        }

        //获取单据状态
        public static string GetItemStatus(string itemId)
        {
            string statusStr = "";
            using (InnovatorSolutionsEntities db = new InnovatorSolutionsEntities())
            {
                var result = (from g in db.WORKFLOW
                              join t in db.WORKFLOW_PROCESS on g.RELATED_ID equals t.ID
                              where g.SOURCE_ID == itemId && t.STATE == "Closed"
                              select g).FirstOrDefault();
                if (result != null)
                {
                    statusStr = "End";
                }
                else
                {
                    var activity = (from g in db.B_PRMANAGE
                                    join t in db.WORKFLOW on g.id equals t.SOURCE_ID
                                    join y in db.WORKFLOW_PROCESS on t.RELATED_ID equals y.ID
                                    join u in db.WORKFLOW_PROCESS_ACTIVITY on y.ID equals u.SOURCE_ID
                                    join i in db.ACTIVITY on u.RELATED_ID equals i.ID
                                    join o in db.ACTIVITY_ASSIGNMENT on i.ID equals o.SOURCE_ID
                                    where i.STATE == "active" && o.CLOSED_BY == null && t.SOURCE_ID == itemId
                                    select i).FirstOrDefault();
                    statusStr = activity.KEYED_NAME;
                }
                return statusStr.Trim();
            }
        }

