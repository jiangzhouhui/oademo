        public static string GetLanguageValueByParam(string chineseValue, string languageCategory, string fileName, string languageType="zh")
        {
            //string ls_lan2 = Request.Headers["Accept-Language"].ToString();
            //string languageType = System.Threading.Thread.CurrentThread.CurrentCulture.Name;
