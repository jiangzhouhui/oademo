        /// <summary>
        /// 创建匹配键值
        /// </summary>
        /// <returns></returns>
        public static Dictionary<string, string> CreateKeyList(string json, string name, bool AddSymbol = false)
        {
            Dictionary<string, string> keyList = new Dictionary<string, string>();
            JObject jsonObj = JObject.Parse(json);
            JObject BasicObj = ((JObject)jsonObj[name]);
            foreach (var item in BasicObj.Properties())
            {
                if (AddSymbol)
                {
                    keyList.Add(item.Name + "：", ((JValue)BasicObj[item.Name]).Value.ToString());
                }
                else
                {
                    keyList.Add(item.Name, ((JValue)BasicObj[item.Name]).Value.ToString());
                }
            }
            return keyList;
        }

        /// <summary>
        /// 根据chineseValue获取值，并将相关的数据，加入缓存
        /// </summary>
        /// <param name="json"></param>
        /// <param name="languageType"></param>
        /// <param name="KeyValue"></param>
        /// <returns></returns>
        public static string GetKeyValue(string json, string languageType, string KeyValue)
        {
            Dictionary<string, string> keyList = new Dictionary<string, string>();
            JObject jsonObj = JObject.Parse(json);
            JObject BasicObj = ((JObject)jsonObj[languageType]);
            foreach (var item in BasicObj.Properties())
            {
                string value = MemoryCacheUtils.Get(item.Name) as string;
                if (string.IsNullOrEmpty(value))
                {
                    MemoryCacheUtils.Set(item.Name, ((JValue)BasicObj[item.Name]).Value.ToString());
                }
            }
            if (((JValue)BasicObj[KeyValue]) != null)
            {
                return ((JValue)BasicObj[KeyValue]).Value.ToString();
            }
            else
            {
                return "";
            }
        }

